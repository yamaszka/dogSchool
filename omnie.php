Nazywam się Barbara Czuba.<br>
Jestem związana z psami od wielu lat. Pierwszy puchar na zawodach Pies Towarzyszący z moją suką cocer spaniel zdobyłam w 2009r.<br><br>

Ukończyłam podyplomowe studia kynologiczne w Szkole Głównej Gospodarstwa Wiejskiego w Warszawie na kierunku "Pies w społeczeństwie. Hodowla i zachowanie" z oceną bardzo dobrą.<br>
Uczestniczyłam w wielu kursach i seminariach prowadzonych przez znanych i cenionych instruktorów psich sportów.<br><br>

Przez kilka lat czynnie uprawiałam sporty kynologiczne z dwoma sukami rasy border collie. Poszerzając swoje horyzonty przygotowywałam je do wielu konkurencji. Agility, Frisbee, Pies Towarzyszący, Obidience, Dogtrekking, Tropienie, Pasienie. Mamy za sobą kilka sukcesów w postaci zdanych egzaminów Agility, PT (Pies Towarzyszący), kwalifikacji do Mistrzostw Polski Frisbee, III miejsca w Mistrzostwach Polski Dogtrekkingu 2015, III miejsca w Pucharze Polski Dogtrekkingu 2015 oraz II miejsce w Mistrzostwach Polski Dogtrekkingu 2018 na dystansie long. <br>
