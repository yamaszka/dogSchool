Prowadzę szkolenia na różnym poziomie zaawansowania. Moim celem jest edukacja właścicieli.<br><br>

Kursy obejmują wiedzę dotyczącą:
<ul>
  <li>pozytywnego oddziaływania na psa</li>
	<li>szybkiego diagnozowania problemów</li>
	<li>różnych metod wychowania</li>
	<li>eliminowania niechcianych i wspierania pożądanych zachowań</li>
	<li>uczenia psa nowych umiejętności</li>
</ul>
Zachęcam aby oddziaływać na psa głównie metodami pozytywnymi, nie wykluczam jednak awersji.<br><br>

Zajęcia praktyczne uzupełniam pogadankami. Odpowiednia wiedza właściciela odgrywa kluczową rolę w wychowaniu psa, który będzie bezproblemowym towarzyszem życia.<br><br>

