<a class="zielony" name="1">1. KTO SIĘ BOI PSA?</a><br>
<a class="zielony">DLA KOGO: </a>wszystkie grupy wiekowe<br>
<a class="zielony">CEL: </a>Profilaktyka pogryzień<br>
Zajęcia mają na celu profilaktykę sytuacji niebezpiecznych z udziałem psów i dzieci poprzez wskazanie sytuacji potencjalnie niebezpiecznych oraz naukę poprawnego zachowania. Uczniowie dowiedzą się o podstawowych sygnałach wysyłanych przez psa. Będą wiedzieli jak zachować się w sytuacji ataku. Podczas zajęć praktycznych nauczą się jak bezpiecznie głaskać psy.<br>
Na zajęciach poruszone zostaną tematy:
<ul>
  <li>odpowiedniego zachowanie w towarzystwie psa w różnych sytuacjach</li>
	<li>poprawnego i bezpiecznego sposobu głaskania</li>
	<li>zachowania w sytuacjach niebezpiecznych</li>
	<li>mowy ciała psa podczas agresji,  lęku i zabawy</li>
	<li>niechęci niektórych zwierząt do kontaktu fizycznego</li>
</ul>


<a class="zielony" name="2A">2 A. PIES W DOMU. PRZYJEMNOŚĆ CZY OBOWIĄZEK?</a><br>
<a class="zielony">DLA KOGO: </a>Gimnazja, licea<br>
<a class="zielony">CEL: </a>Przeciwdziałanie bezdomności psów poprzez edukację<br>

Zajęcia mają na celu przeciwdziałanie porzucaniu zwierząt. Jedną z głównych przyczyn pozbywania się psa jest nieodpowiedzialna decyzja o jego nabyciu. Uczniowie dowiedzą się o problemach związanych z posiadaniem psa. Nauczą się rozwiązywać niektóre z nich. Dowiedzą się jakie obowiązki nakłada prawo na właścicieli psów oraz jakie są ewentualne kary za ich nieprzestrzeganie.<br>
Na zajęciach poruszone zostaną tematy:
<ul>
  <li>uciążliwości związanych z posiadaniem psa</li>
	<li>problemów wynikających z obecności psa w domu</li>
	<li>bezdomności wynikającej z pochopnej decyzji kupna psa</li>
	<li>prawnych obowiązków właściciela psa i kar związanych z ich nieprzestrzeganiem</li>
	<li>prawnych i moralnych zasad rozmnażania zwierząt</li>
</ul>

<a class="zielony" name="2B">2 B. RASY PSÓW, KTÓREGO WYBRAĆ</a><br>
<a class="zielony">DLA KOGO: </a>klasy 4-6, gimnazja, licea<br>
<a class="zielony">CEL: </a>Przeciwdziałanie bezdomności psów poprzez edukację<br>

Zajęcia mają na celu umożliwienie zadowalającego wyboru rasy psa poprzez uświadomienie różnorodności charakterów i użytkowości psów. Podczas zajęć uczniowie będą przyporządkowywać wybrane rasy psów do określonych osobowości człowieka. Dowiedzą się jakie są cechy mieszańców oraz jak wybrać organizację adopcyjną.
Na zajęciach poruszone zostaną tematy:
<ul>
  <li>ras psów, ich pochodzenie i przeznaczenie</li>
	<li>doboru odpowiedniej rasy i użyteczności do oczekiwań przyszłego właściciela</li>
	<li>adopcji psa ze schroniska lub fundacji</li>
</ul>

<a class="zielony" name="3A">3 A. PROBLEMY PSÓW CYWILIZOWANEGO ŚWIATA</a><br>
<a class="zielony">DLA KOGO: </a>wszystkie grupy wiekowe<br>
<a class="zielony">CEL: </a>Promocja aktywności fizycznej wśród młodzieży w formie sportów kynologicznych<br>

Zajęcia maja na celu zachęcanie młodzieży do podejmowania aktywności z psem. Uczniowie dowiedzą się, że prawie każdy pies potrzebuje różnorodnej aktywności oraz że zapewnienie mu jedzenia i odpoczynku nie jest wystarczające. Dodatkowo uczniowie nauczą się nazw podstawowych dyscyplin kynologicznych oraz zapoznają się z podstawowym potrzebnym sprzętem.

Na zajęciach zostaną poruszone tematy:
<ul>
  <li>udomowienia psa</li>
	<li>użytkowości jaką pies przez wieki oferował człowiekowi</li>
	<li>współczesnych zadań psów</li>
	<li>rodzajów sportów i rekreacji kynologicznej</li>
</ul>

<a class="zielony" name="3B">3 B. Z PSEM NA ZAWODY</a><br>
<a class="zielony">DLA KOGO: </a>klasy 4-6, gimnazja, licea<br>
<a class="zielony">CEL: </a>Promocja aktywności fizycznej wśród młodzieży w formie sportów kynologicznych<br>

Zajęcia maja na celu przybliżenie wybranych sportów kynologicznych- dogtrekkingu oraz agility. Dogtrekking jest łatwą dyscypliną dostępna dla każdego właściciela psa. Uczniowie poznają podstawowe reguły zmagań. Zostaną wspomniane także inne dyscypliny zaprzęgowe w których Polacy odnoszą światowe sukcesy. Dodatkowo uczniowie obejrzą pokaz agility (sportu trudnego i wymagającego długiego szkolenia), podczas którego poznają podstawowe umiejętności psów w tej dyscyplinie oraz obejrzą przebieg całego toru będący ukoronowaniem szkolenia psa.

Na zajęciach zostaną poruszone tematy:
<ul>
  <li>sprzętu potrzebnego do dogtrekkingu</li>
	<li>zasad przeprowadzania zawodów</li>
	<li>promocji dogtrekkingu we wszystkich grupach społecznych</li>
	<li>sukcesów polaków w międzynarodowych zawodach zaprzęgowych</li>
	<li>	odbędzie się pokaz agility (tylko na trawiastym terenie), prezentujący poszczególne umiejętności psów oraz pokonanie całego toruv</li>
</ul>


<a class="zielony" name="4">4. JAK ROZMAWIAĆ TRZEBA Z PSEM</a><br>
<a class="zielony">DLA KOGO: </a>wszystkie grupy wiekowe<br>
<a class="zielony">CEL: </a>Nauka podstaw szkolenia psów<br>

Zajęcia mają na celu przybliżenie podstawowych reguł szkolenia psów. Uczniowie nauczą się jak motywować psy do wspólnej aktywności. Dowiedzą się jak używać słów oraz gestów w kontakcie z psem. Dodatkowo poznają tajniki szkolenia pozytywnego. Dowiedzą się jak nauczyć psa podstawowych komend. Prowadzący zwróci także uwagę na samopoczucie psa podczas ćwiczeń.

NA zajęciach zostaną poruszone tematy:
<ul>
  <li>motywacji psów</li>
	 <li>rozumienia ludzkiej mowy przez psy</li>
	  <li>używania różnych tonów głosu oraz gestów w kontakcie z psem</li>
		 <li>szkolenia pozytywnego i tradycyjnego</li>
		  <li>nauki podstawowych komend</li>
			 <li>dbałości o samopoczucie psa podczas szkoleń</li>
</ul>

